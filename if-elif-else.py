n=int(input("Enter marks:"))
if n>=90 and n<=100:
    print("Marks:",n,"A")
elif n>=80 and n<=89:
    print("Marks:",n,"B")
elif n>=70 and n<=79:
    print("Marks:",n,"C")
elif n>=60 and n<=69:
    print("Marks:",n,"D")
elif n>=50 and n<=59:
    print("Marks:",n,"E")
else:
    print("Marks:",n,"F")

